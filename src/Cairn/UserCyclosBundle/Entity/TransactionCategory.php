<?php

namespace Cairn\UserCyclosBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * TransactionCategory
 *
 * @ORM\Table(name="transaction_category")
 * @ORM\Entity(repositoryClass="Cairn\UserCyclosBundle\Repository\TransactionCategoryRepository")
 */
class TransactionCategory
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=20)
     */
    private $name;



    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return TransactionCategory
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

}

